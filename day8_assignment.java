
import java.util.Scanner;

public class day8_assignment {
    public static void main(String[] args) {

        // Q29 - Half Pyramid Pattern
        System.out.println("Q29 - Half Pyramid Pattern");
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }

        System.out.println();

        // Q30 - Number Triangle
        System.out.println("Q30 - Number Triangle");
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j);
            }
            System.out.println();
        }

        System.out.println();

        // Q31 - Character Triangle
        System.out.println("Q31 - Character Triangle");
        for (char i = 'A'; i <= 'E'; i++) {
            for (char j = 'A'; j <= i; j++) {
                System.out.print(j);
            }
            System.out.println();
        }

        System.out.println();

        // Q32 - Repeated Number Pattern
        System.out.println("Q32 - Repeated Number Pattern");
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(i);
            }
            System.out.println();
        }
  
   
   
   
    }
} 
    

